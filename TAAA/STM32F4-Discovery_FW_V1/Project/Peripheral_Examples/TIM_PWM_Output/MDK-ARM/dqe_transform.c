#include "stm32f4_discovery.h"
#include "stm32f4xx.h"
#include "dqe_transform.h"
#include "math.h"

/***********************************************************/
/*                       _____________                     */
/*                      |             |                    */
/*           VDs ======>| VDQs->VDQe  |=====> VDe          */
/*           VQs ======>|             |=====> VQe          */
/*         angle ======>|_____________|                    */
/*                                                         */
/***********************************************************/

void DQe_calc(DQe *p)
{
	
				p->VDe=((p->VDs*cosf(p->angle))+(p->VQs*sinf(p->angle)));
				p->VQe=((p->VDs*(-sinf(p->angle)))+(p->VQs*cosf(p->angle)));
	
}
